package com.example.demo.sample.model;

public class mainModel {
	String jobId;
	String skillCd;
	String skillNm;
	String skillEff;
	String skillTime;
	String major;
	public String getJobId() {
		return jobId;
	}
	public void setJobId(String jobId) {
		this.jobId = jobId;
	}
	public String getSkillCd() {
		return skillCd;
	}
	public void setSkillCd(String skillId) {
		this.skillCd = skillId;
	}
	public String getSkillNm() {
		return skillNm;
	}
	public void setSkillNm(String skillNm) {
		this.skillNm = skillNm;
	}
	public String getSkillEff() {
		return skillEff;
	}
	public void setSkillEff(String skillEff) {
		this.skillEff = skillEff;
	}
	public String getSkillTime() {
		return skillTime;
	}
	public void setSkillTime(String skillTime) {
		this.skillTime = skillTime;
	}
	public String getMajor() {
		return major;
	}
	public void setMajor(String major) {
		this.major = major;
	}
}
